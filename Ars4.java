public class Ars4 {
    public static void main(String[] args) {
        int nums[][]={ {1,2},
                       {3,4},
                       {8,6} };

            for(int i=0;i<3;i++) {
                for(int j=0;j<2;j++) {
                    System.out.print(nums[i][j]);
                }
                System.out.println();
            }            
    }
    
}
