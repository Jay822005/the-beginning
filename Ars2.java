public class Ars2 {
    public static void main(String[] args) {
        int nums[] = {2,4,1,5};
        String names[] = {"shrey","yash","sahil"};
        for(int i=0;i<4;i++) {
            System.out.println(nums[i]);
        }
        int j=0;
        while(j<3) {
            System.out.println(names[j]);
            j++;
        }
    }
    
}
