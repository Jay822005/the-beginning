public class Ars6 {
    public static void main(String[] args) {
        int nums[][]={{1,4,5,7}
                     ,{5,6,3,8},
                      {1,0,0,3}};

        for(int i=0;i<3;i++) {
            for(int j=0;j<4;j++) {
                System.out.print(nums[i][j]);
            }
            System.out.println();
        }              
    }
    
}
