/*if the input value is odd or even. If it’s odd print “Odd”, otherwise
print “Even”.Note: Input value will be between & and &0^6.*/
public class ifelse {
    public static void main(String[] args) {
        int value = 10;
        if(value%2 == 0) {
            System.out.println("value "+value+" is even");
        }
        else {
            System.out.println("value "+value+" is odd");
        }
    }
    
}
